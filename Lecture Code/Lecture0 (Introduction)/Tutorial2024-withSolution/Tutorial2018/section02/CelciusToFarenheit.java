
public class CelciusToFarenheit {

	public static void main(String[] args) {
          final double TEMP_CONVERT = 1.8;  
         final double TEMP_CONVERT_2 = 32;
		
		 double cel = 33;
         double fa;
         
         fa = (cel*TEMP_CONVERT)+TEMP_CONVERT_2;
         System.out.println(fa);

	}

}
